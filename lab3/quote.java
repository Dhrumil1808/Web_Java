package cs1220.lab3;

public class quote {

	String quotes,author;
	public quote(String quotes, String author)
	{
		this.quotes=quotes;
		this.author=author;
	}
	public String getQuotes() {
		return quotes;
	}
	public void setQuotes(String quotes) {
		this.quotes = quotes;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	
}
